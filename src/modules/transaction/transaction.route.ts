import express from 'express';
import { auth } from '../../middlewares/auth';
import {
    addCreatedByIdToBody,
    addDeletedByToBody,
    addUpdatedByIdToBody,
} from '../../middlewares/addUserToBody'
import {validate} from '../../middlewares/validate';
import {transactionController} from './transaction.controller';
import {transactionValidation} from './transaction.validation';
import { AdminPermission, Permission } from '../../middlewares/checkPermission';

const router = express.Router()

router
    .route('/')
    .post(auth(),AdminPermission(),addCreatedByIdToBody,validate(transactionValidation.createOne),transactionController.createOne)
    .get(auth(), AdminPermission(), validate(transactionValidation.getList),transactionController.getList);

router.route('/all').get(auth(), AdminPermission(), validate(transactionValidation.getAll),transactionController.getAll);

router
    .route('/:transactionId')
    .get(auth(), AdminPermission(), validate(transactionValidation.getOne), transactionController.getOne)
    .patch(auth(), Permission.EmployeePermission(), addUpdatedByIdToBody, validate(transactionValidation.updateOne), transactionController.updateOne)
    .delete(auth(), Permission.EmployeePermission(), addDeletedByToBody, validate(transactionValidation.deleteOne), transactionController.deleteOne);

export const transactionRoute = router;

